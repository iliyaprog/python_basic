import re


car_nums = 'А578ВЕ777 ОР233787 К901МН666 СТ46599 СНИ2929П777 666АМР666'

res_1 = re.findall(r'\b[АВЕКМНОРСТУХ]\d{3}[АВЕКМНОРСТУХ]{2}\d{2,3}\b', car_nums)
print('Список номеров частных автомобилей:', res_1)

res_2 = re.findall(r'\b[АВЕКМНОРСТУХ]{2}\d{3}\d{2,3}\b', car_nums)
print('Список номеров такси:', res_2)

